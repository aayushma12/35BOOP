// In Java, both ' (single quote) and " (double quote) are used to denote characters or strings,
// but they have different meanings and usages.

// Single Quote ': It is used to represent a single character literal. 
// For example, 'a' represents the character 'a'. Single quotes are used only for single characters, not for strings.
// Double Quote ": It is used to represent string literals. 
// For example, "hello" represents the string "hello". Double quotes are used for strings that can contain multiple characters.




